static constexpr size_t kGlyphTableSize = sizeof(kGlyphTable) / sizeof(kGlyphTable[0]);

uint16_t toLowerToUpper(uint16_t codepoint) {
  if (codepoint >= 'a' && codepoint <= 'z') {
    return static_cast<uint16_t>(codepoint - 32);
  }
  if (codepoint >= 0x0430 && codepoint <= 0x044F) {
    return static_cast<uint16_t>(codepoint - 0x20);
  }
  if (codepoint == 0x0451) {
    return 0x0401;
  }
  return codepoint;
}

