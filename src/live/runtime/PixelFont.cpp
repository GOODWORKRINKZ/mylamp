const FontGlyph* getFontGlyph(uint16_t codepoint) {
  const uint16_t normalized = toLowerToUpper(codepoint);

  for (size_t i = 0; i < kGlyphTableSize; ++i) {
    if (kGlyphTable[i].codepoint == normalized) {
      return &kGlyphTable[i].glyph;
    }
  }

  return nullptr;
}

uint16_t decodeUtf8Char(const std::string& text, size_t& position) {
  if (position >= text.size()) {
    return 0;
  }

  const auto byte0 = static_cast<uint8_t>(text[position]);
  if (byte0 < 0x80) {
    ++position;
    return byte0;
  }

  if ((byte0 & 0xE0) == 0xC0 && position + 1 < text.size()) {
    const auto byte1 = static_cast<uint8_t>(text[position + 1]);
    position += 2;
    return static_cast<uint16_t>(((byte0 & 0x1F) << 6) | (byte1 & 0x3F));
  }

  if ((byte0 & 0xF0) == 0xE0 && position + 2 < text.size()) {
    const auto byte1 = static_cast<uint8_t>(text[position + 1]);
    const auto byte2 = static_cast<uint8_t>(text[position + 2]);
    position += 3;
    return static_cast<uint16_t>(((byte0 & 0x0F) << 12) | ((byte1 & 0x3F) << 6) | (byte2 & 0x3F));
  }

  ++position;
  return 0xFFFD;
}

